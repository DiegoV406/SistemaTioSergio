    private void btnVoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVoltarActionPerformed
        Usuario usuario = SessaoUsuario.getUsuarioLogado();
        if (usuario != null) {
            String setor = usuario.getSetor();
            this.dispose();
            TelaMenu tM = new TelaMenu();
            tM.configurarPermissaoEstoque(setor);
            tM.configurarPermissaoAdministração(setor);
            tM.setTitle("Menu");
            tM.setLocationRelativeTo(null);
            tM.pack();
            tM.setVisible(true);
        }
    }//GEN-LAST:event_btnVoltarActionPerformed

   /**
     * Metodo responsavel por mostrar valores em uma tabela
     */
   private void preencherTabela() {
        try {
            ProdutoDao dao = new ProdutoDao();
            dao.conectar();

            List<Produto> listaProduto = dao.getProduto();

            DefaultTableModel tabelaProduto = (DefaultTableModel) tblTabela.getModel();
            tabelaProduto.setNumRows(0);
            tabelaProduto.setRowCount(0);

            tblTabela.setRowSorter(new TableRowSorter(tabelaProduto));

            for (Produto c : listaProduto) {
                Object[] obj = new Object[]{
                    c.getId(),
                    c.getNome(),
                    c.getQuantidade(),
                    c.getValor(),
                    c.getData(),
                    c.getFornecedor()
                
                };
                tabelaProduto.addRow(obj);
            }
            dao.desconectar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar produtos: " + e.getMessage());
        }
    }
   
    
