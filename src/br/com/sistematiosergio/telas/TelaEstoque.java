    /**
     * Botão responsavel por excluir 
     * @param evt 
     */
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            int linhaSelecionada = tblTabela.getSelectedRow();

            if (linhaSelecionada == -1) {
                JOptionPane.showMessageDialog(this, "Selecione um produto para excluir.");
                return;
            }

            int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir?", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION);

            if (confirmacao == JOptionPane.YES_OPTION) {
                int id = (int) tblTabela.getValueAt(linhaSelecionada, 0); // ID está na primeira coluna

                ProdutoDao dao = new ProdutoDao();
                dao.conectar();
                boolean sucesso = dao.excluir(id);
                dao.desconectar();

                if (sucesso) {
                    ((DefaultTableModel) tblTabela.getModel()).removeRow(linhaSelecionada);
                    JOptionPane.showMessageDialog(this, "Produto excluído com sucesso.");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir produto.");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

